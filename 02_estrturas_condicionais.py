# Algoritmo 02: Estruturas condicionais
idade = 18
gosto_de_fruta = True

# O comando if é seguido de uma comparação
# Se o resultado da comparação for verdadeiro, o código abaixo do if é executado
if gosto_de_fruta == True:
    print('Eu gosto de fruta')



if idade >= 18:
    # Se a idade for maior que 18, execute esses passos:
    print('Posso beber')
    print('Posso dirigir')
    print('Posso trabalhar')

else:
    # O comando else pode ser entendido como "do contrário"
    # Do contrário, execute esses passos:
    print('Voce é crianca')
    
    
    
# Nota: o comando else só pode ser dado depois de um comando if
